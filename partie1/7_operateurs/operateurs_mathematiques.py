"""Il existe différents opérateurs en Python dont les opérateurs mathématiques"""

print(5 + 3) # renvoie 8
print(3 - 2) # renvoie 1
print(4 * 5) # renvoie 20
print(6 / 2) # renvoie 3.0 car une division renvoie toujours un float

"""On peut également utiliser ces opérateurs avec des str"""

print("Hello " + "world!") # renvoie "Hello world!"
print("Python" * 3) # renvoie "PythonPythonPython"

"""Il existe également des opérateurs un peu moins connus"""

print(10 % 3) # le modulo : il renvoie le reste d'une division euclidienne : ici 1 (10 / 3 = 3 reste 1)
print(10 // 3) # la division entière : elle renvoie le quotion d'une division euclidienne : ici 3
print(2 ** 4) # la puissance : renvoie le résultat de 2 * 2 * 2 * 2
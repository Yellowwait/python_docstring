"""Les opérateurs d'assignation permettent d'attribuer une valeur à une variable."""

i = 0
i = i + 1 # on incrémente i de 1 donc i vaut maintenant 1
i += 1 # idem mais plus agréable à lire et plus concis
i -= 1 # i = i - 1
i *= 1 # i = i * 1
i /= 1 # i = i / 1
i %= 1 # i = i % 1
i //= 1 # i = i // 1
i **= 1 # i = i ** 1

a = 5
a += 10
print(a)
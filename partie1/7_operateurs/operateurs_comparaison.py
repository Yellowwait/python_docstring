"""Voici la liste des opérateurs de comparaison"""

a = 5
b = 10

print(a > b) # a est plus grand que b --> False
print(a < b) # a est plus petit que b --> True
print(a >= b) # a est supérieur ou égal à b --> False
print(a <= b) # a est inférieur ou égal à b --> True
print(a == b) # a est strictement égal à b --> False (compare aussi le type de donnée)
print(a != b) # a est différent de b --> True
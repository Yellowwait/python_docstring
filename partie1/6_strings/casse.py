"""Il existe plusieurs méthode pour changer la casse"""

a = "Bonjour"
print(a.upper()) # upper() permet de tout mettre en majuscule
print(a.lower()) # lower() permet de tout mettre en minuscule

b = "tout le monde"
print(b.capitalize()) # capitalize() permet de transformer la première lettre en majuscule
print(b.title()) # title() transforme la première lettre de chaque mot en majuscule
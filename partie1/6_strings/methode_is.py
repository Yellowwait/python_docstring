"""Il existe plusieurs méthodes "is" pour vérifier ce que contient une str"""

print("bonjour".islower()) # islower() vérifie si toute la str est en minuscule
print("bonjour".isupper()) # isupper() vérifie si toute la str est en majuscule
print("Bonjour Tout Le Monde".istitle()) # istitle() vérifie si la première lettre de chaque mot est en majuscule
print("50x".isdigit()) # isdigit() vérifie si toute la str est composée uniquement de chiffres